// Função para obter a contagem de países por continente para a tela de seleção
export function getCountriesByContinent() {
    const continents = {
        africa: [],
        europe: [],
        asia: [],
        americas: [],
        oceania: []
    };
    
    for (const [countryName, countryData] of Object.entries(countryDetails)) {
        if (continents[countryData.continent]) {
            continents[countryData.continent].push({
                name: countryName,
                ...countryData
            });
        }
    }
    
    return continents;
}
